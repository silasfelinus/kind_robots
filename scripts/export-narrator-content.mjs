#!/usr/bin/env node
// scripts/export-narrator-content.mjs
//
// Dumps the live NarratorTopic + NarratorThread content out of the database and
// into version-controlled seed files, so the narrator conversation trees are
// readable/editable in the repo instead of trapped in the DB.
//
// Output (re-seedable via POST /api/bots/topics and POST /api/bots/threads):
//   stores/seeds/narratorTopics.ts   -> export const narratorTopicSeeds
//   stores/seeds/narratorThreads.ts  -> export const narratorThreadSeeds
//
// Usage:
//   KR_API_TOKEN=... node scripts/export-narrator-content.mjs
//   APP_BASE_URL=https://kind-robots.vercel.app KR_API_TOKEN=... node scripts/export-narrator-content.mjs
//
// Reads the GET endpoints (server/api/bots/topics.get.ts, threads.get.ts) with
// ?includeInactive=true so soft-deleted rows are captured too.

import { writeFile, mkdir } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const BASE = (
  process.env.APP_BASE_URL || 'https://kind-robots.vercel.app'
).replace(/\/$/, '')
const TOKEN = process.env.KR_API_TOKEN || ''
const __dirname = dirname(fileURLToPath(import.meta.url))
const seedsDir = resolve(__dirname, '../stores/seeds')

async function getJson(path) {
  const res = await fetch(`${BASE}${path}`, {
    headers: TOKEN ? { Authorization: `Bearer ${TOKEN}` } : {},
  })
  if (!res.ok) {
    throw new Error(`GET ${path} -> HTTP ${res.status}`)
  }
  const body = await res.json()
  if (body && body.success === false) {
    throw new Error(`GET ${path} -> ${body.message || 'request failed'}`)
  }
  return Array.isArray(body?.data) ? body.data : []
}

function parseStarterPrompts(value) {
  if (value == null) return null
  if (Array.isArray(value)) return value
  if (typeof value === 'string') {
    const trimmed = value.trim()
    if (!trimmed) return null
    try {
      const parsed = JSON.parse(trimmed)
      return Array.isArray(parsed) ? parsed : null
    } catch {
      return null
    }
  }
  return null
}

function banner(name) {
  return `// ${name}\n// AUTO-GENERATED by scripts/export-narrator-content.mjs — do not hand-edit the\n// shape; edit content then re-run, or edit and re-seed via the batch endpoints\n// (POST /api/bots/topics, POST /api/bots/threads, both accept { dryRun: true }).\n`
}

async function main() {
  const [topics, threads] = await Promise.all([
    getJson('/api/bots/topics?includeInactive=true'),
    getJson('/api/bots/threads?includeInactive=true'),
  ])

  const topicSeeds = topics.map((t) => ({
    slug: t.slug,
    title: t.title,
    subtitle: t.subtitle ?? null,
    description: t.description ?? null,
    icon: t.icon ?? null,
    prompt: t.prompt,
    sampleUserPrompt: t.sampleUserPrompt ?? null,
    sortOrder: typeof t.sortOrder === 'number' ? t.sortOrder : 0,
    isPublic: t.isPublic ?? true,
    isActive: t.isActive ?? true,
  }))

  const threadSeeds = threads.map((t) => ({
    botName: t.Bot?.name ?? null,
    topicSlug: t.Topic?.slug ?? null,
    title: t.title ?? null,
    openingText: t.openingText ?? '',
    guidance: t.guidance ?? null,
    starterPrompts: parseStarterPrompts(t.starterPrompts),
    sortOrder: typeof t.sortOrder === 'number' ? t.sortOrder : 0,
    isActive: t.isActive ?? true,
  }))

  const unresolved = threadSeeds.filter((t) => !t.botName || !t.topicSlug)
  if (unresolved.length) {
    console.warn(
      `WARNING: ${unresolved.length} thread(s) missing botName/topicSlug — they will not re-seed cleanly.`,
    )
  }

  await mkdir(seedsDir, { recursive: true })

  const topicsFile = `${banner('stores/seeds/narratorTopics.ts')}\nexport const narratorTopicSeeds = ${JSON.stringify(topicSeeds, null, 2)} as const\n`
  const threadsFile = `${banner('stores/seeds/narratorThreads.ts')}\nexport const narratorThreadSeeds = ${JSON.stringify(threadSeeds, null, 2)} as const\n`

  await writeFile(resolve(seedsDir, 'narratorTopics.ts'), topicsFile, 'utf8')
  await writeFile(resolve(seedsDir, 'narratorThreads.ts'), threadsFile, 'utf8')

  console.log(
    `Exported ${topicSeeds.length} topic(s) -> stores/seeds/narratorTopics.ts`,
  )
  console.log(
    `Exported ${threadSeeds.length} thread(s) -> stores/seeds/narratorThreads.ts`,
  )
}

main().catch((error) => {
  console.error(error?.message || error)
  process.exit(1)
})
